def save_bitmap_as_image(bitmap, char, output_dir="characters"):
    img_array = np.array(bitmap, dtype=np.uint8) * 255  # convert 0/1 to 0/255
    img = Image.fromarray(img_array, mode='L')
    img.save(f"{output_dir}/{char}.png")

# Create output directory
os.makedirs("characters", exist_ok=True)

# Save each character
for char, bitmap in CHAR_BITMAPS.items():
    save_bitmap_as_image(bitmap, char)
